#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_lst;
	t_list	*new_node;
	void	*content_mapped;

	new_lst = NULL;
	if (!lst && !f)
		return (NULL);
	while (lst)
	{
		content_mapped = (*f)(lst->content);
		new_node = ft_lstnew(content_mapped);
		if (!new_node)
		{
			ft_lstclear(&new_lst, del);
			return (NULL);
		}
		ft_lstadd_back(&new_lst, new_node);
		lst = lst->next;
	}
	return (new_lst);
}
